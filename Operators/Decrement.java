package com.xworkz.corejava.Operators;

public class Decrement {

	public static void main(String [] args){

	       int a = 12;
	       int b = 20;
	       int c = 33;
	       c = a;
	       --a;
	System.out.println(c++);
	System.out.println(a++);
	System.out.println(b--);
	System.out.println((--b)+(--c));
	System.out.println(a);
	System.out.println(b);
	System.out.println(c);
	}
	}
